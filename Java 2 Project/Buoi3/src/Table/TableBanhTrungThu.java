    public void fillTableData(ArrayList<BanhTrungThu> danhSach) {
        DefaultTableModel model = (DefaultTableModel) tblBanhTrungThu.getModel();
        model.setRowCount(0);
        for (BanhTrungThu banhTrungThu : danhSach) {
            model.addRow(new Object[]{banhTrungThu.getId(), banhTrungThu.getTen(), banhTrungThu.getGia(), banhTrungThu.getLoaiNhan()});
        }
        tblBanhTrungThu.setModel(model);
    }

