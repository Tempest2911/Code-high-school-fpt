    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        int ID = Integer.parseInt(txtID.getText());
        String Name = txtName.getText();
        float Price = Float.parseFloat(txtPrice.getText());
        String Nhan = txtNhan.getText();

        listBanh.add(new BanhTrungThu(ID, Name, Price, Nhan));
        fillTableData(listBanh);

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int ID = Integer.parseInt(txtID.getText());
        BanhTrungThu btt = null;

        for (BanhTrungThu banhTrungThu : listBanh) {
            if (banhTrungThu.getId() == ID) {
                btt = banhTrungThu;
            }
        }
        listBanh.remove(btt);
        fillTableData(listBanh);

    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int ID = Integer.parseInt(txtID.getText());
        String Name = txtName.getText();
        float Price = Float.parseFloat(txtPrice.getText());
        String Nhan = txtNhan.getText();

        for (BanhTrungThu banhTrungThu : listBanh) {
            if (banhTrungThu.getId() == ID) {
                banhTrungThu.setTen(Name);
                banhTrungThu.setGia(Price);
                banhTrungThu.setLoaiNhan(Nhan);
            }
        }
        fillTableData(listBanh);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblBanhTrungThuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBanhTrungThuMouseClicked
        int selectedIndex = tblBanhTrungThu.getSelectedRow();
        String id = tblBanhTrungThu.getValueAt(selectedIndex, 0).toString();
        String Name = tblBanhTrungThu.getValueAt(selectedIndex, 1).toString();
        String Price = tblBanhTrungThu.getValueAt(selectedIndex, 2).toString();
        String Nhan = tblBanhTrungThu.getValueAt(selectedIndex, 3).toString();
        txtID.setText(id);
        txtName.setText(Name);
        txtPrice.setText(Price);
        txtNhan.setText(Nhan);


    }//GEN-LAST:event_tblBanhTrungThuMouseClicked

