    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int id = Integer.parseInt(txtID.getText());
        String ten = txtName.getText();
        float gia = Float.parseFloat(txtPrice.getText());
        int Size = Integer.parseInt(txtSize.getText());

        listAoPolo.add(new KIEMTRAAAAAAAA(id, ten, gia, Size));

        fillTableData(listAoPolo);

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int id = Integer.parseInt(txtID.getText());
        KIEMTRAAAAAAAA KT = null;

        for (KIEMTRAAAAAAAA kiemtraaaaaaaa : listAoPolo) {
            if (kiemtraaaaaaaa.getId() == id) {
                KT = kiemtraaaaaaaa;
            }
        }

        listAoPolo.remove(KT);

        fillTableData(listAoPolo);
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblAoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAoMouseClicked
        int selectedIndex = tblAo.getSelectedRow();
        String id = tblAo.getValueAt(selectedIndex, 0).toString();
        String Name = tblAo.getValueAt(selectedIndex, 1).toString();
        String Price = tblAo.getValueAt(selectedIndex, 2).toString();
        String Size = tblAo.getValueAt(selectedIndex, 3).toString();
        txtID.setText(id);
        txtName.setText(Name);
        txtPrice.setText(Price);
        txtSize.setText(Size);
    }//GEN-LAST:event_tblAoMouseClicked

