    public void fillTableData(ArrayList<KIEMTRAAAAAAAA> danhSach) {
        DefaultTableModel model = (DefaultTableModel) tblAo.getModel();
        model.setRowCount(0);
        for (KIEMTRAAAAAAAA kiemtraaaaaaaa : danhSach) {
            model.addRow(new Object[]{kiemtraaaaaaaa.getId(), kiemtraaaaaaaa.getTen(), kiemtraaaaaaaa.getGia(), kiemtraaaaaaaa.getSize()});
        }
        tblAo.setModel(model);
    }

