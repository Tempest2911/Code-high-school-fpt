    public void fillTableData(ArrayList<Model> listDT) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblDT.getModel();
        defaultTableModel.setRowCount(0);
        for (Model model : listDT) {
            defaultTableModel.addRow(new Object[]{
                model.getId(),
                model.getTen(),
                model.isKheSim() ? "Có" : "Không",
                model.getHang(),
                model.getGia()
            });
        }

    }

    public Model readData() {
        int id = Integer.parseInt(txtID.getText());
        String ten = txtTen.getText();
        boolean kheSim = rdoCo.isSelected() ? true : false;
        String hang = cboHang.getSelectedItem().toString();
        float gia = Float.parseFloat(txtGia.getText());

        return new Model(id, ten, kheSim, hang, gia);
    }

