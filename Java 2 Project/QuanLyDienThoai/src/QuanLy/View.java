    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (txtID.getText().isEmpty() || txtTen.getText().isEmpty() || txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phai dien day du");
        } else {
            quanLyDT.addDT(readData());
            fillTableData(quanLyDT.getAll());
        }


    }//GEN-LAST:event_btnThemActionPerformed

    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        if (txtID.getText().isEmpty() || txtTen.getText().isEmpty() || txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phai co thong tin tren bang");
        } else {
        quanLyDT.changeDT(readData());
        fillTableData(quanLyDT.getAll());
        }
    }//GEN-LAST:event_btnChangeActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (txtID.getText().isEmpty() || txtTen.getText().isEmpty() || txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phai co thong tin tren bang");
        } else {
        quanLyDT.deleteDT(readData().getId());
        fillTableData(quanLyDT.getAll());
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblDTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDTMouseClicked
        int SelectedIndex = tblDT.getSelectedRow();

        txtID.setText(tblDT.getValueAt(SelectedIndex, 0).toString());
        txtTen.setText(tblDT.getValueAt(SelectedIndex, 1).toString());
        if (tblDT.getValueAt(SelectedIndex, 2).toString().equals("Có")) {
            rdoCo.setSelected(true);
        } else {
            rdoKhong.setSelected(true);
        }
        cboHang.setSelectedItem(tblDT.getValueAt(SelectedIndex, 3).toString());
        txtGia.setText(tblDT.getValueAt(SelectedIndex, 4).toString());
    }//GEN-LAST:event_tblDTMouseClicked

