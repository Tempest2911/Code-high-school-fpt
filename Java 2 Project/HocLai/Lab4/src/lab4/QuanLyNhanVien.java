    private void btnKiemtraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKiemtraActionPerformed

        String fullname = jTextField1.getText().trim();
        String dateText = jTextField2.getText().trim();

        boolean hasError = false;

        jTextField1.setBackground(Color.WHITE);
        jTextField2.setBackground(Color.WHITE);
        jTextField3.setBackground(Color.WHITE);

        if (fullname.length() == 0) {
            showError(jTextField1, "Họ tên không được để trống!");
            hasError = true;
        } else if (!fullname.matches("^[^0-9]+$")) {
            showError(jTextField1, "Họ tên không được chứa số!");
            hasError = true;
        }

        if (dateText.isEmpty()) {
            showError(jTextField2, "Ngày sinh không được để trống!");
            hasError = true;
        } else {
            try {
                Xdate.parse(dateText, "dd-MM-yyyy");
            } catch (RuntimeException e) {
                showError(jTextField2, "Ngày sinh không đúng định dạng! (dd-MM-yyyy)");
                hasError = true;
            }
        }

        try {
            String salaryText = jTextField3.getText().trim();

            if (salaryText.isEmpty()) {
                showError(jTextField3, "Lương không được để trống!");
                return;
            }

            double salary = Double.parseDouble(salaryText);

            if (salary <= 0) {
                showError(jTextField3, "Lương phải là số dương!");
                return;
            }
        } catch (NumberFormatException e) {
            showError(jTextField3, "Lương phải là số hợp lệ!");
        }

        // Nếu không có lỗi
        if (!hasError) {
            JOptionPane.showMessageDialog(this, "Dữ liệu hợp lệ!");
        }
    }//GEN-LAST:event_btnKiemtraActionPerformed

