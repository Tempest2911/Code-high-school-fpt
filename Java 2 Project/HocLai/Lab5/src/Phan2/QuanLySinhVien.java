    // üìù Ghi danh s√°ch nh√¢n vi√™n v√†o file TXT
    private void saveToFile() {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(FILE_PATH))) {
            for (Staff s : list) {
                bw.write(s.getFullname() + "," + s.getSalary());
                bw.newLine();
            }
            JOptionPane.showMessageDialog(this, "L∆∞u file th√†nh c√¥ng!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "L·ªói khi ghi file!");
        }
    }

    // üìñ ƒê·ªçc danh s√°ch nh√¢n vi√™n t·ª´ file TXT
    private void readFromFile() {
        File file = new File(FILE_PATH);
        if (!file.exists()) {
            JOptionPane.showMessageDialog(this, "File kh√¥ng t·ªìn t·∫°i!");
            return;
        }

        list.clear();
        try (BufferedReader br = new BufferedReader(new FileReader(file))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length == 2) {
                    String name = parts[0].trim();
                    double salary = Double.parseDouble(parts[1].trim());
                    list.add(new Staff(name, salary));
                }
            }
            FillTableData(getAll());
            JOptionPane.showMessageDialog(this, "ƒê·ªçc file th√†nh c√¥ng!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "L·ªói khi ƒë·ªçc file!");
        }
    }

