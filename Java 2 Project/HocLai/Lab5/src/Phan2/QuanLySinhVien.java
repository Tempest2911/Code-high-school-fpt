    private void FillTableData(ArrayList<Staff> s) {
        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        model.setRowCount(0);
        for (Staff sv : s) {
            model.addRow(new Object[]{sv.getFullname(), sv.getSalary()});
        }
    }

