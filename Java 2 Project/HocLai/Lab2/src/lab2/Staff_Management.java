    public void FillTableData(ArrayList<StaffModel> m) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblStaff.getModel();
        defaultTableModel.setRowCount(0);
        for (StaffModel model : m) {
            defaultTableModel.addRow(new Object[]{
                model.getId(),
                model.getName(),
                model.getCountry(),
                model.getGender(),
                model.getStatus()
            });
        }
    }

