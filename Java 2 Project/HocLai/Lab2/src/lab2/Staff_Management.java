    private void tblStaffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStaffMouseClicked
        int sp = tblStaff.getSelectedRow();
        txt_ID.setText(tblStaff.getValueAt(sp, 0).toString());
        txt_Name.setText(tblStaff.getValueAt(sp, 1).toString());
        cbo_Country.setSelectedItem(tblStaff.getValueAt(sp, 2).toString());
        if (tblStaff.getValueAt(sp, 3).equals("Male")) {
            rdo_Male.setSelected(true);
        } else {
            rdo_Female.setSelected(true);
        }
        if (tblStaff.getValueAt(sp, 4).equals("Single")) {
            CheckBox.setSelected(true);
        } else {
            CheckBox.setSelected(false);
        }

    }//GEN-LAST:event_tblStaffMouseClicked

