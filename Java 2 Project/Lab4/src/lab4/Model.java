public class Model {

   private int id;
   private String ten;
   private boolean gioiTinh;
   private String chucVu;

    public Model() {
    }

    public Model(int id, String ten, boolean gioiTinh, String chucVu) {
        this.id = id;
        this.ten = ten;
        this.gioiTinh = gioiTinh;
        this.chucVu = chucVu;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public boolean isGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(boolean gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getChucVu() {
        return chucVu;
    }

    public void setChucVu(String chucVu) {
        this.chucVu = chucVu;
    }
   
   

}
