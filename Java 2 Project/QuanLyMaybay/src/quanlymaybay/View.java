    public boolean checkLoi() {
        boolean hasError = false;

        if (Integer.parseInt(txtNam.getText()) < 1900) {
            JOptionPane.showMessageDialog(this, "Năm sản xuất phải lớn hơn 1900");
            hasError = true;
        }

        if (txtName.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(this, "Tên không được chứa số");
            hasError = true;
        }

        return hasError;
    }

    public void showRow(int index) {
        txtID.setText(tblMaybay.getValueAt(index, 0).toString());
        txtName.setText(tblMaybay.getValueAt(index, 1).toString());
        if (tblMaybay.getValueAt(index, 2).toString().equals("Hoat dong")) {
            rdoHoatDong.setSelected(true);
        } else {
            rdoKhongHoatDong.setSelected(true);
        }
        cboHang.setSelectedItem(tblMaybay.getValueAt(index, 3).toString());
        txtNam.setText(tblMaybay.getValueAt(index, 4).toString());
    }

    public void FillTableData(ArrayList<Model> list) {
        DefaultTableModel model = (DefaultTableModel) tblMaybay.getModel();
        model.setRowCount(0);
        for (Model model1 : list) {
            model.addRow(new Object[]{
                model1.getId(),
                model1.getTen(),
                model1.getTrangthai(),
                model1.getHang(),
                model1.getNamSanXuat()
            });
        }
    }

    public Model readForm() {
        return new Model(
                txtID.getText(),
                txtName.getText(),
                rdoHoatDong.isSelected() ? "Hoat dong" : "Khong hoat dong",
                cboHang.getSelectedItem().toString(),
                Integer.parseInt(txtNam.getText())
        );
    }

