    private void btnDocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocFileActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Ban co muon doc file khong", "Xac nhan", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            showRow(tblMaybay.getRowCount() - 1);
            s.docFile();
            FillTableData(s.getall());
        } else {
            JOptionPane.showMessageDialog(this, "Huỷ");
        }

    }//GEN-LAST:event_btnDocFileActionPerformed

