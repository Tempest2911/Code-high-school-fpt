    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Lấy đường dẫn file

        String fileName = "D:\\CODE\\Java 3 Project\\ASM1\\taikhoan.txt";
        try (Stream<String> stream = Files.lines(Paths.get(fileName))) { //Tạo ra 1 Stream để đọc từng dòng của File
            stream.forEach(line -> {
                System.out.println(line);
                allAccount.add(line); //Thêm từng dòng vào arraylist chức các account
            });
        } catch (Exception e) {
            e.printStackTrace();
        }

        String LoginInfo = txt_Username.getText() + "," + txt_Password.getText();
        boolean checkLogIn = false;
        for (int i = 0; i < allAccount.size(); i++) {
            if (allAccount.get(i).equals(LoginInfo) && cbo_CheckBox.isSelected()) {
                JOptionPane.showMessageDialog(rootPane, "Successfully");
                openMainFrame(txt_Username.getText());
                checkLogIn = true;
                break;
            }
        }

        if (!checkLogIn)
            JOptionPane.showMessageDialog(rootPane, "Failled");
    }//GEN-LAST:event_jButton1ActionPerformed

