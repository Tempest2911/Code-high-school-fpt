    public void FillTableData(ArrayList<Model> m) {
        DefaultTableModel table = (DefaultTableModel) tbl.getModel();
        table.setRowCount(0);

        for (Model model : m) {
            table.addRow(new Object[]{
                model.getId(),
                model.getName(),
                model.getStatus(),
                model.getNumber()
            });
        }

    }

    public Model readForm() {
        return new Model(txtID.getText(), txtName.getText(), rdoHd.isSelected() ? "Hoạt động" : "Đã giải tán", Integer.parseInt(txtNumber.getText()));
    }

    public boolean check() {
        boolean error = false;

        if (txtID.getText().isBlank() || txtID.getText().isEmpty() || txtName.getText().isBlank() || txtName.getText().isEmpty() || txtNumber.getText().isBlank() || txtNumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phải điền hết thông tin");
            error = true;
        }

        if (!txtNumber.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Phải đúng định dạng số");
            error = true;
        }

        if (txtName.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(this, "Tên không được chứa số");
            error = true;
        }

        if (rdoGt.isSelected() == false && rdoHd.isSelected() == false) {
            JOptionPane.showMessageDialog(this, "Phải chọn trạng thái");
            error = true;
        }

        return error;
    }

