    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int row = tbl.getSelectedRow();

        txtID.setText(tbl.getValueAt(row, 0).toString());
        txtName.setText(tbl.getValueAt(row, 1).toString());
        if (tbl.getValueAt(row, 2).toString().equals("Hoạt động")) {
            rdoHd.setSelected(true);
        } else {
            rdoGt.setSelected(true);
        }
        txtNumber.setText(tbl.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblMouseClicked

