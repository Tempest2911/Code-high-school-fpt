    private void CheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckActionPerformed

        if (checkLoi()) {
            return;
        } else {

            String main = TextMain.getText();
            String chuoiSo[] = main.split(",");
//
            String max = chuoiSo[0];
            for (int i = 1; i < chuoiSo.length; i++) {
                if (Integer.parseInt(chuoiSo[i]) > Integer.parseInt(max)) {
                    max = chuoiSo[i];
                }
            }
            BigNumber.setText("" + max);
//
            String min = chuoiSo[0];
            for (int i = 1; i < chuoiSo.length; i++) {
                if (Integer.parseInt(chuoiSo[i]) < Integer.parseInt(min)) {
                    min = chuoiSo[i];
                }
            }
            SmallNumber.setText("" + min);
//

            int Top2Max = Integer.MIN_VALUE;
            for (int i = 0; i < chuoiSo.length; i++) {
                if (Integer.parseInt(chuoiSo[i]) > Top2Max && Integer.parseInt(chuoiSo[i]) < Integer.parseInt(max)) {
                    Top2Max = Integer.parseInt(chuoiSo[i]);
                }
            }
            Top2BigNumber.setText("" + Top2Max);
//

            float sum = 0;
            float avgNumber = 0;
            int dem = 0;

            for (int i = 0; i < chuoiSo.length; i++) {
                sum += Integer.parseInt(chuoiSo[i]);
                dem++;
            }

            avgNumber = sum / dem;

            AVGNUM.setText("" + avgNumber);
        }

    }//GEN-LAST:event_CheckActionPerformed

    private void SortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SortActionPerformed
        if (checkLoi()) {
            return;
        } else {
            String main = TextMain.getText();
            String[] MainText = main.split(",");

// Khởi tạo mảng số nguyên
            int[] intArray = new int[MainText.length];

// Chuyển đổi từng phần tử chuỗi thành số nguyên và lưu vào mảng số nguyên
            for (int i = 0; i < MainText.length; i++) {
                intArray[i] = Integer.parseInt(MainText[i].trim());
            }

// Sắp xếp mảng số nguyên
            Arrays.sort(intArray);

// Xây dựng chuỗi đã sắp xếp và loại bỏ các số trùng nhau
            StringBuilder sortedText = new StringBuilder();
            sortedText.append(intArray[0]); // Thêm phần tử đầu tiên

            for (int i = 1; i < intArray.length; i++) {
                if (intArray[i] != intArray[i - 1]) { // Kiểm tra số trùng nhau
                    sortedText.append(",").append(intArray[i]);
                }
            }

// Đặt lại văn bản của TextMain mà không có dấu ngoặc vuông và không có số trùng nhau
            TextMain.setText(sortedText.toString());

        }

    }//GEN-LAST:event_SortActionPerformed

