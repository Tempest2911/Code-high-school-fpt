    public int clickCount = 0;

    //Táº¡o 1 textfield, 1 button, khi click vao button lan sole thi hien thi thong tin cua text field lan so chan thi xoa textfield
    public void initControl() {

        TextField txt = new TextField();
        Button btn = new Button("Click me");

        btn.setSize(100, 50);
        btn.setLocation(10, 50);
        this.add(btn);

        txt.setSize(100, 20);
        txt.setLocation(10, 20);
        this.add(txt);

        btn.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                clickCount++;
                if (clickCount % 2 == 0) {
                    JOptionPane.showMessageDialog(rootPane, txt.getText());
                } else {
                    txt.setText("");
                }
            }
        });
    }

