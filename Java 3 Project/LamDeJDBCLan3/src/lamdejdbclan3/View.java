    public void FillTable(ArrayList<Model> m) {
        DefaultTableModel table = (DefaultTableModel) tbl_nv.getModel();
        table.setRowCount(0);
        for (Model model : m) {
            table.addRow(new Object[]{
                model.getId(),
                model.getName(),
                model.getStatus(),
                model.getMoney()
            });
        }
    }

    public Model readForm() {
        return new Model(
                txt_ID.getText(),
                txt_Name.getText(),
                rdo_nghi.isSelected() ? "Nghỉ làm" : "Làm việc",
                Double.parseDouble(txt_Money.getText()));
    }

