    private void tbl_nvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_nvMouseClicked
        int row = tbl_nv.getSelectedRow();

        txt_ID.setText(tbl_nv.getValueAt(row, 0).toString());
        txt_Name.setText(tbl_nv.getValueAt(row, 1).toString());
        if (tbl_nv.getValueAt(row, 2).toString().equals("Nghỉ làm")) {
            rdo_nghi.setSelected(true);
        } else {
            rdo_lam.setSelected(true);
        }
        txt_Money.setText(tbl_nv.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tbl_nvMouseClicked

    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
        if (txt_Max.getText().isBlank() || txt_Max.getText().isEmpty() || txt_Min.getText().isBlank() || txt_Min.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Khoong dc trong");
        } else {
            FillTable(s.search(Double.parseDouble(txt_Min.getText()), Double.parseDouble(txt_Max.getText())));
        }


    }//GEN-LAST:event_btn_SearchActionPerformed

