    private boolean validateInput(String username, String password, String fullname, Date birthday, String phone, String email) {
        if (username.isEmpty() || password.isEmpty() || phone.isEmpty() || email.isEmpty() || fullname.isEmpty() || birthday == null) {
            JOptionPane.showMessageDialog(this, "HÃ£y Ä‘iá»n Ä‘áº§y Ä‘á»§ thÃ´ng tin", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // 2. Kiá»ƒm tra máº­t kháº©u: Ãt nháº¥t 1 chá»¯ hoa, 1 sá»‘, tá»‘i Ä‘a 25 kÃ½ tá»±
        if (!password.matches("^(?=.*[A-Z])(?=.*\\d).{1,25}$")) {
            JOptionPane.showMessageDialog(this, "Máº­t kháº©u pháº£i cÃ³ Ã­t nháº¥t má»™t chá»¯ hoa, má»™t sá»‘ vÃ  tá»‘i Ä‘a 25 kÃ½ tá»±!");
            return false;
        }

        // 3. Kiá»ƒm tra sá»‘ Ä‘iá»‡n thoáº¡i VN (Báº¯t Ä‘áº§u báº±ng 0, cÃ³ 10 sá»‘)
        if (!phone.matches("^0\\d{9}$")) {
            JOptionPane.showMessageDialog(this, "Sá»‘ Ä‘iá»‡n thoáº¡i pháº£i cÃ³ 10 chá»¯ sá»‘ vÃ  báº¯t Ä‘áº§u báº±ng 0!");
            return false;
        }

        // 4. Kiá»ƒm tra email (pháº£i cÃ³ @ vÃ  Ä‘Ãºng Ä‘á»‹nh dáº¡ng)
        if (!email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
            JOptionPane.showMessageDialog(this, "Email khÃ´ng há»£p lá»‡!");
            return false;
        }
        return true;
    }

    private boolean isUsernameTaken(String username) {
        String checkQuery = "SELECT COUNT(*) FROM TaiKhoan WHERE Username = ?";
        try (Connection con = DriverManager.getConnection("jdbc:sqlserver://Tempest:1433;databasename=DU_AN_MAU;trustServerCertificate=true;user=sa;password=123456"); PreparedStatement stmt = con.prepareStatement(checkQuery)) {
            stmt.setString(1, username);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                return rs.getInt(1) > 0;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return false;
    }

